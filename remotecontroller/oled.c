static void OLED_Cmd(uint8_t cmd)
{
    I2C_Start();
    I2C_Write((OLED_ADDR << 1) | 0); // write
    I2C_Write(0x00);                  // control byte: command
    I2C_Write(cmd);
    I2C_Stop();
}

static void OLED_Data(uint8_t data)
{
    I2C_Start();
    I2C_Write((OLED_ADDR << 1) | 0);
    I2C_Write(0x40);                  // control byte: data
    I2C_Write(data);
    I2C_Stop();
}

void OLED_Init(void)
{
    I2C_Init();
    _delay_ms(100);

    OLED_Cmd(0xAE); // display off
    OLED_Cmd(0x20); // memory addressing mode
    OLED_Cmd(0x00); // horizontal addressing
    OLED_Cmd(0xB0); // page start address
    OLED_Cmd(0xC8); // COM scan direction (flip vertical)
    OLED_Cmd(0x00); // low column address
    OLED_Cmd(0x10); // high column address
    OLED_Cmd(0x40); // start line
    OLED_Cmd(0x81); // contrast
    OLED_Cmd(0xFF); // max contrast
    OLED_Cmd(0xA1); // segment re-map
    OLED_Cmd(0xA6); // normal display (A7 = inverted)
    OLED_Cmd(0xA8); // multiplex ratio
    OLED_Cmd(0x3F); // 64 rows
    OLED_Cmd(0xA4); // output follows RAM
    OLED_Cmd(0xD3); // display offset
    OLED_Cmd(0x00);
    OLED_Cmd(0xD5); // clock divide
    OLED_Cmd(0xF0);
    OLED_Cmd(0xD9); // pre-charge
    OLED_Cmd(0x22);
    OLED_Cmd(0xDA); // COM pins
    OLED_Cmd(0x12);
    OLED_Cmd(0xDB); // VCOMH deselect
    OLED_Cmd(0x20);
    OLED_Cmd(0x8D); // charge pump
    OLED_Cmd(0x14); // enable
    OLED_Cmd(0xAF); // display ON
}

void OLED_Clear(void)
{
    for (uint8_t page = 0; page < 8; page++)
    {
        OLED_Cmd(0xB0 + page);
        OLED_Cmd(0x00);
        OLED_Cmd(0x10);
        for (uint8_t col = 0; col < 128; col++)
            OLED_Data(0x00);
    }
}

void OLED_SetCursor(uint8_t row, uint8_t col)
{
    // row 0-7, col 0-20 (approx, each char is 6px wide)
    uint8_t px = col * 6;
    OLED_Cmd(0xB0 + row);
    OLED_Cmd(0x00 + (px & 0x0F));
    OLED_Cmd(0x10 + (px >> 4));
}

void OLED_PrintChar(char c)
{
    if (c < 0x20 || c > 0x5A) c = ' ';
    uint8_t idx = c - 0x20;
    for (uint8_t i = 0; i < 5; i++)
        OLED_Data(pgm_read_byte(&font[idx][i]));
    OLED_Data(0x00); // spacing column
}

void OLED_PrintString(const char* str)
{
    while (*str) OLED_PrintChar(*str++);
}

void OLED_DrawBitmap(const uint8_t *bmp, uint8_t pages, uint8_t cols)
{
    for (uint8_t page = 0; page < pages; page++)
    {
        OLED_Cmd(0xB0 + page);
        OLED_Cmd(0x00);
        OLED_Cmd(0x10);
        for (uint8_t col = 0; col < cols; col++)
            OLED_Data(pgm_read_byte(&bmp[page * cols + col]));
    }
}