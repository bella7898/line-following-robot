volatile uint8_t carrier_enabled = 0;
ISR(TIMER2_COMPA_vect)
{
    if (carrier_enabled)
        PORTB ^= (1 << PB2);
    else
        PORTB &= ~(1 << PB2);
}
void IR_Off(void)
{
    carrier_enabled = 0;
    PORTB &= ~(1 << PB2);
}
void IR_On(void)
{
    carrier_enabled = 1;
}
void IR_Init(void)
{
    DDRB  |=  (1 << PB2);
    PORTB &= ~(1 << PB2);
    TCCR2A = (1 << WGM21);
    TCCR2B = (1 << CS20);
    OCR2A  = 209;
    TIMSK2 = (1 << OCIE2A);
    IR_Off();
}
void Configure_Pins(void)
{
    // Explicitly set button pins as INPUT
    DDRD  &= ~((1 << PD3) | (1 << PD2) | (1 << PD4));  // inputs
    //DDRD  |=   (1 << PD4);                               // PD4 output
    DDRB  &=  ~(1 << PB1);   // PB1 input
    PORTB |=   (1 << PB1);   // pullup on PB1

    DDRC  &= ~(1 << PC2);    // PC2 input
    PORTC |=  (1 << PC2);    // pullup on PC2

    // Pull-ups on all button pins
	PORTD |= (1 << PD3) | (1 << PD2) | (1 << PD4);
	
	TIMSK1 &= ~(1 << ICIE1);  // disable Timer1 input capture interrupt
	TCCR1B &= ~(1 << ICES1);  // disable input capture edge select
}
void ADC_Init(void)
{
    ADMUX = (1 << REFS0) | (1 << MUX1) | (1 << MUX0);
    ADCSRA = (1 << ADEN) | (1 << ADPS2) | (1 << ADPS1) | (1 << ADPS0);
}
uint16_t ADC_Read(uint8_t channel)
{
    ADMUX = (1 << REFS0) | (channel & 0x07);
    ADCSRA |= (1 << ADSC);
    while (ADCSRA & (1 << ADSC));
    return ADC;
}
const char* Get_Joystick_State(uint16_t x, uint16_t y, uint8_t sw_pressed)
{
    if (sw_pressed)              return "S";
    if (y < CENTER - THRESHOLD)  return "R";
    if (y > CENTER + THRESHOLD)  return "L";
    if (x < CENTER - THRESHOLD)  return "F";
    if (x > CENTER + THRESHOLD)  return "B";
    return "";
}
const char* Get_Joystick_Label(const char* state)
{
	if (state[0] == 'S') return "STOP";
   
	if (state[0] == 'F') return "FORWARD";
   
	if (state[0] == 'B') return "BACK";
   
	if (state[0] == 'L') return "LEFT";
   
	if (state[0] == 'R') return "RIGHT";
   
	return "";
}
void waitms(int ms) { _delay_ms(1); while (--ms) _delay_ms(1); }
void Log_Print(const char *msg)
{
    if (!log_started) {
        OLED_Clear();
        log_started = 1;
    }
    if (log_row >= LOG_ROWS) {
        OLED_Clear();
        log_row = 0;
    }
    OLED_SetCursor(log_row, 0);
    OLED_PrintString(msg);
    log_row++;
}
void Check_Buttons(uint8_t pd3, uint8_t pd5, uint8_t pd6, uint8_t pb1)
{
    static uint8_t last_pd3 = 0, last_pd5 = 1, last_pd6 = 1, last_pb1 = 0;
    static uint8_t stable_pd3 = 0, stable_pd5 = 1, stable_pd6 = 1, stable_pb1 = 0;
    static uint8_t count_pd5 = 3, count_pd6 = 3;  // start already stable

    static uint8_t prev_pd3 = 0, prev_pd5 = 1, prev_pd6 = 1, prev_pb1 = 0;
    
	if (pd5 == last_pd5) { if (++count_pd5 >= 2) stable_pd5 = pd5; }
	else count_pd5 = 0;
	
	if (pd6 == last_pd6) { if (++count_pd6 >= 5) stable_pd6 = pd6; }
	else count_pd6 = 0;
	
	if (pd3 == last_pd3) stable_pd3 = pd3;
	if (pb1 == last_pb1) stable_pb1 = pb1;
	
	last_pd3 = pd3; last_pd5 = pd5;
	last_pd6 = pd6; last_pb1 = pb1;

    if (stable_pd5 && !prev_pd5) {
        IR_On();
        current_mode = (current_mode + 1) % NUM_MODES;
        char buf[20];
        sprintf(buf, ">> MODE: %s", mode_names[current_mode]);
        Log_Print(buf);
    }
    if (!stable_pd5 && prev_pd5) IR_Off();

    if (stable_pd3 && !prev_pd3) { putchar('T'); IR_On();  Log_Print(">> TURN 180"); }
    if (!stable_pd3 && prev_pd3)   IR_Off();
	if (stable_pb1 && !prev_pb1) {
	    IR_On();
	    switch(current_mode){
	        case 0: putchar('M'); break;
	        case 1: putchar('N'); break;
	        case 2: putchar('A'); break;
	        case 3: putchar('X'); break;
	        case 4: putchar('Y'); break;
	        case 5: putchar('Z'); break;
	        default: break;
	    }
	    char buf[20];
	    sprintf(buf, ">> SEL: %s", mode_names[current_mode]);
	    Log_Print(buf);
	}
	if (!stable_pb1 && prev_pb1) IR_Off();

    prev_pd3 = stable_pd3; prev_pd5 = stable_pd5;
    prev_pd6 = stable_pd6; prev_pb1 = stable_pb1;
}
